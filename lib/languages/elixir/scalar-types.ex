# Integers
age = 30
sum = age + 5  # 35

# Floats
height = 1.75
area = height * 2.0  # 3.5

# Booleans
is_active = true
is_valid = is_active and age > 18

# Atoms (symbols)
status = :ok
result = {:ok, "Success"}

# Strings (binary)
name = "Alice"
greeting = "Hello, " <> name  # String concatenation

# String interpolation
message = "Name: #{name}, Age: #{age}"

# Charlists (list of integers)
charlist = 'ABC'  # [65, 66, 67]

